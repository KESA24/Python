# Working with python numbers
print(2)
print(2+3.4)
print(2*3.4)
print(3*(4+5))
# Modulus gives us remainder after division, ans below =1
print(10%3)

my_num = 5
print(my_num)

# To print out a number in a string, you must use the "str" function otw the programme will throw an error
print(str(my_num) + " is my favorite number")

# Absolutevalue => abs
my_numb = -5
print(abs(my_numb))

# Power
print(pow(4, 6))
print(pow(3, 2))

# Bigger/smaller num
print(max(3, 5, 7))
print(min(4, 7, 1))

# Round off numbers
print(round(3.7))

# Following gves us more math functions
from math import *
print(floor(3.7))
print(ceil(3.7))
print(sqrt(36))




